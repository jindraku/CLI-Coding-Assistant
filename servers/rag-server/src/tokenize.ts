const STOPWORDS = new Set([
  "the",
  "a",
  "an",
  "and",
  "or",
  "but",
  "if",
  "then",
  "else",
  "is",
  "are",
  "was",
  "were",
  "be",
  "been",
  "to",
  "of",
  "in",
  "on",
  "for",
  "with",
  "as",
  "by",
  "at",
  "from",
  "it",
  "this",
  "that",
  "these",
  "those",
  "your",
  "you",
  "we",
  "our",
  "they",
  "their",
  "its",
  "not",
  "can",
  "could",
  "should",
  "would",
  "will",
  "may",
  "might",
  "about",
  "into",
  "over",
  "under",
  "up",
  "down",
]);

export function tokenize(text: string): string[] {
  return text
    .toLowerCase()
    .split(/[^a-z0-9]+/)
    .filter((token) => token.length > 1 && !STOPWORDS.has(token));
}
